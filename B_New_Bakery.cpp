void solve()
{
    long long n,a,b;
    cin>>n>>a>>b;

    long long k1=max(0LL,(2*(b-a)+1)/2);

    k1=min(k1,min(n,b));
    long long k2=max(0LL,k1+1);
    k2=min(k2,min(n,b));
    long long res1=k1*(b+1)-((k1*(k1+1)))/2+(n-k1)*a;
    long long res2=k2*(b+1)-((k2*(k2+1)))/2+(n-k2)*a;

    cout<<max(res1,res2)<<endl;
}